public class Modelo_Venda {

    private int quantiten;
    private String datasaida;
    private float valor_venda;
    private String nomecliente;
    private String nomeProduto;

    /**
     * @return the quantiten
     */
    public int getQuantiten() {
        return quantiten;
    }

    /**
     * @param quantiten the quantiten to set
     */
    public void setQuantiten(int quantiten) {
        this.quantiten = quantiten;
    }

    /**
     * @return the datasaida
     */
    public String getDatasaida() {
        return datasaida;
    }

    /**
     * @param datasaida the datasaida to set
     */
    public void setDatasaida(String datasaida) {
        this.datasaida = datasaida;
    }

    /**
     * @return the valor_venda
     */
    public float getValor_venda() {
        return valor_venda;
    }

    /**
     * @param valor_venda the valor_venda to set
     */
    public void setValor_venda(float valor_venda) {
        this.valor_venda = valor_venda;
    }

    /**
     * @return the nomecliente
     */
    public String getNomecliente() {
        return nomecliente;
    }

    /**
     * @param nomecliente the nomecliente to set
     */
    public void setNomecliente(String nomecliente) {
        this.nomecliente = nomecliente;
    }

    /**
     * @return the nomeProduto
     */
    public String getNomeProduto() {
        return nomeProduto;
    }

    /**
     * @param nomeProduto the nomeProduto to set
     */
    public void setNomeProduto(String nomeProduto) {
        this.nomeProduto = nomeProduto;
    }
    
    
    
}
